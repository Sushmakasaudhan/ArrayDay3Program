package com.bridgelabz;

public class Printarray {
    public static void main(String[] args) {
        int[] arr = new int[] {1,2,3,4,5};

        System.out.println("Elements is given array:");
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + "");
        }
    }
}